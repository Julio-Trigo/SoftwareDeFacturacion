    private void AgregarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AgregarBtnMouseClicked
        int codigo = Integer.parseInt(CodigoTxt.getText());
        float cantidad = Float.parseFloat(CantidadTxt.getText());

        try {
            Connection xion = Conexion.getConexion();
            Statement run = xion.createStatement();
            ResultSet rs = run.executeQuery("SELECT * FROM Producto WHERE id_producto= '" + codigo + "'");

            if (rs.next()) {
                ProductoTxt.setText(rs.getString("Nombre"));
                PrecioTxt.setText(rs.getString("Precio"));

                float PrecioPivote = Float.parseFloat(PrecioTxt.getText());

                float PrecioXCantidad = PrecioPivote * cantidad;

                String[] compra = new String[3];
                compra[0] = ProductoTxt.getText();
                compra[1] = CantidadTxt.getText();
                compra[2] = Float.toString(PrecioXCantidad);
                modelo.addRow(compra);

                sumar = sumar + PrecioXCantidad;

                CodigoTxt.setText("");
                CantidadTxt.setText("");
                ProductoTxt.setText("");
                PrecioTxt.setText("");

            } else {
                JOptionPane.showMessageDialog(null, "No existe el coduigo del producto");
                CodigoTxt.setText("");
                CantidadTxt.setText("");
            }

            PrecioTotal.setText(Float.toString(sumar));

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
        
    }//GEN-LAST:event_AgregarBtnMouseClicked

    private void PagarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PagarBtnMouseClicked
        int contar = modelo.getRowCount();

        try {
            Connection xion = Conexion.getConexion();

            for (int i = 0; i < contar; i++) {
                PreparedStatement ps = xion.prepareStatement("UPDATE Producto SET Cantidad = Cantidad - " + modelo.getValueAt(i, 1) + " Where Nombre = '" + modelo.getValueAt(i, 0) + "'");
                ps.executeUpdate();

            }
            JOptionPane.showMessageDialog(null, "Compra Realizada");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
        //Factura
        String Nombre = NombreTxt.getText();
        String FechaActual = formato.format(fecha);
        Float total = Float.parseFloat(PrecioTotal.getText());
        try{
            
            Connection xion = Conexion.getConexion();
            PreparedStatement ps = xion.prepareStatement("INSERT INTO Facturas (Nombre_cliente, Fecha, Total) VALUES (?,?,?)");
            ps.setString(1, Nombre);
            ps.setString(2, FechaActual);
            ps.setFloat(3, total);
            ps.executeUpdate();
            
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }

        //Enable(True)
        CedulaTxt.setEnabled(true);
        IngresarBtn.setEnabled(true);

        //Enable(False)
        NombreTxt.setEnabled(false);
        ApellidoTxt.setEnabled(false);
        RegistrarBtn.setEnabled(false);
        TelefonoTxt.setEnabled(false);
        CantidadTxt.setEnabled(false);
        CodigoTxt.setEnabled(false);
        ProductoTxt.setVisible(false);
        PrecioTxt.setVisible(false);
        AgregarBtn.setEnabled(false);
        PrecioTotal.setEditable(false);
        PagarBtn.setEnabled(false);
        
        for(int i = contar-1; i >= 0; i-- ){
            modelo.removeRow(i);
        }
        
        //borrar todo
        CedulaTxt.setText("");
        NombreTxt.setText("");
        ApellidoTxt.setText("");
        TelefonoTxt.setText("");
        PrecioTotal.setText("");
        
        sumar = 0;
    }//GEN-LAST:event_PagarBtnMouseClicked


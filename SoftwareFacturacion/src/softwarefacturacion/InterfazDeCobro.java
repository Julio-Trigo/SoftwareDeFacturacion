    private void IngresarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_IngresarBtnMouseClicked

        if("".equals(CedulaTxt.getText())){
            JOptionPane.showMessageDialog(null, "Ingrese una cedula");
        } else {
             int cedula = Integer.parseInt(CedulaTxt.getText());
             
             
            try {
            Connection xion = Conexion.getConexion();
            Statement run = xion.createStatement();
            ResultSet rs = run.executeQuery("SELECT * FROM Cliente WHERE Cedula= '" + cedula + "'");

            if (rs.next()) {
                NombreTxt.setText(rs.getString("Nombre"));
                ApellidoTxt.setText(rs.getString("Apellido"));
                CedulaTxt.setEnabled(false);
                IngresarBtn.setEnabled(false);
                CodigoTxt.setEnabled(true);
                CantidadTxt.setEnabled(true);
                AgregarBtn.setEnabled(true);
                PagarBtn.setEnabled(true);
            } else {
                NombreTxt.setEnabled(true);
                ApellidoTxt.setEnabled(true);
                RegistrarBtn.setEnabled(true);
                TelefonoJLabel.setVisible(true);
                TelefonoTxt.setEnabled(true);
                IngresarBtn.setEnabled(false);
                CodigoTxt.setEnabled(true);
                CantidadTxt.setEnabled(true);
                AgregarBtn.setEnabled(true);
                PagarBtn.setEnabled(true);

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        }
    }//GEN-LAST:event_IngresarBtnMouseClicked

    private void RegistrarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RegistrarBtnMouseClicked
        int cedula = Integer.parseInt(CedulaTxt.getText());
        String Nombre = NombreTxt.getText();
        String Apellido = ApellidoTxt.getText();
        String telefono = TelefonoTxt.getText();

        try {
            Connection xion = Conexion.getConexion();
            PreparedStatement ps = xion.prepareStatement("INSERT INTO Cliente (Nombre, Apellido, Cedula, Telefono) VALUES (?,?,?,?)");
            ps.setString(1, Nombre);
            ps.setString(2, Apellido);
            ps.setInt(3, cedula);
            ps.setString(4, telefono);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Cliente Agregado");

            CedulaTxt.setEnabled(false);
            NombreTxt.setEnabled(false);
            ApellidoTxt.setEnabled(false);
            TelefonoTxt.setEnabled(false);

            CodigoTxt.setEnabled(true);
            CantidadTxt.setEnabled(true);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_RegistrarBtnMouseClicked


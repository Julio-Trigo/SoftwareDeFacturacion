    private void InventarioAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_InventarioAdminMouseClicked
        
        try{
            int fila = InventarioAdmin.getSelectedRow();
            int id = Integer.parseInt(InventarioAdmin.getValueAt(fila, 0).toString());
            
            PreparedStatement ps;
            ResultSet rs;
            
            Connection xion = Conexion.getConexion();
            ps = xion.prepareStatement("SELECT Nombre, Precio, Cantidad, Categoria FROM Producto WHERE id_producto=?");
            ps.setInt(1, id);
            rs = ps.executeQuery();
            
            while(rs.next()){
                idApoyo.setText(String.valueOf(id));
                NombreApoyo.setText(rs.getString("Nombre"));
                precioApoyo.setText(rs.getString("Precio"));
                CantidadApoyo.setText(rs.getString("Cantidad"));
                CategoriaApoyo.setText(rs.getString("Categoria"));
            }
            
           
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_InventarioAdminMouseClicked


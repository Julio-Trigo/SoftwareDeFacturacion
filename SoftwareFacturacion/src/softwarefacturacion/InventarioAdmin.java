    private void CargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) InventarioAdmin.getModel();
        modeloTabla.setRowCount(0);
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        int[] anchos = {10, 100, 30, 50, 100};
        for(int i = 0; i < InventarioAdmin.getColumnCount(); i++){
            InventarioAdmin.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        
        try{
            
            Connection xion = Conexion.getConexion();
            ps = xion.prepareStatement("SELECT id_producto, Nombre, Precio, Cantidad, Categoria FROM Producto");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while(rs.next()){
                Object[] fila = new Object[columnas];
                for(int indice = 0; indice<columnas; indice++){
                    fila[indice] = rs.getObject(indice + 1);
                }
                modeloTabla.addRow(fila);
            }
            
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
